package dataimpl;

public class PawnLocate {
        int x;
        int y;
        public PawnLocate(int xx,int yy){
                x = xx;
                y = yy;
        }
        public int getX() {
                return x;
        }
        public void setX(int x) {
                this.x = x;
        }
        public int getY() {
                return y;
        }
        public void setY(int y) {
                this.y = y;
        }
}